package de.szut.marcel713;

public class GanzeZahlen {

	public static void main(String[] args) {
		
		int zahl1 = 999999;
		long zahl2 = 999999;
		
		System.out.println(zahl1 * zahl1);
		System.out.println(zahl2 * zahl2);
		
		//Die Variable int kann nur einen bestimmten Zahlenraum ausgeben die mit dieser Gleichung überschritten wird.
		// Die Variable long kann auch nur einen bestimmten Zahlenraum ausgeben die sie aber mit dieser Gleichung ausgibt.

	}

}
